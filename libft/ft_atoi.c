#include "libft.h"

int	ft_atoi(const char *str)
{
	int	minus;
	int	answer;

	minus = 1;
	answer = 0;
	while (*str == '\t' || *str == '\n' || *str == '\v'
		|| *str == '\f' || *str == '\r' || *str == ' ')
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			minus *= -1;
		str++;
	}
	if (!(*str >= '0' && *str <= '9'))
		return (0);
	while (*str >= '0' && *str <= '9')
	{
		answer = (answer * 10) + (*str - 48);
		str++;
	}
	return (answer * minus);
}
